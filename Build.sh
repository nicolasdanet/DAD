#! /usr/bin/env bash

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

# Must be executed at the same level.

rep=$(pwd); [ "${0%/*}" = "." ] || exit 1

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

# Basic platform handler.

case "$OSTYPE"  in
    darwin*)    cd DAD/Builds/MacOSX ;
                xcodebuild -configuration Release ;
                rm -r build/XCBuildData ;
                rm -r build/DAD.build ;;
    linux*)     cd DAD/Builds/LinuxMakefile ;
                make CONFIG=Release ;
                rm -r build/intermediate ;;
    *)          exit 1 ;;
esac

